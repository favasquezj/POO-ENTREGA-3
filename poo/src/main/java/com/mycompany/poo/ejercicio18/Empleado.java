public class Empleado {
    private String codigoEmpleado;
    private String nombres;
    private int horasLaboradasMes;
    private double valorHoraTrabajada;
    private float porcentajeRetencionFuente;

    public String getCodigoEmpleado() {
        return codigoEmpleado;
    }

    public void setCodigoEmpleado(String codigoEmpleado) {
        this.codigoEmpleado = codigoEmpleado;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public int getHorasLaboradasMes() {
        return horasLaboradasMes;
    }

    public void setHorasLaboradasMes(int horasLaboradasMes) {
        this.horasLaboradasMes = horasLaboradasMes;
    }

    public double getValorHoraTrabajada() {
        return valorHoraTrabajada;
    }

    public void setValorHoraTrabajada(double valorHoraTrabajada) {
        this.valorHoraTrabajada = valorHoraTrabajada;
    }

    public float getPorcentajeRetencionFuente() {
        return porcentajeRetencionFuente;
    }

    public void setPorcentajeRetencionFuente(float porcentajeRetencionFuente) {
        this.porcentajeRetencionFuente = porcentajeRetencionFuente;
    }
   
}
